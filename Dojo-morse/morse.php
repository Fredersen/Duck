<?php

function morseToCode($str)
{

    $morse = [
        "-.-.--" => "!",
        "....-" => "4",
        "..--.." => "?",
        "--..--" => ",",
        ".-.-.-" => ".",
        "..---" => "2",
        "...--" => "3",
        "--..." => "7",
        "-...." => "6",
        "....." => "5",
        "---.." => "8",
        "-..." => "B",
        "----." => "9",
        ".--." => "P",
        "-----" => "0",
        "--.." => "Z",
        "-.--" => "Y",
        "-..-" => "X",
        "-.-." => "C",
        "...-" => "V",
        ".----" => "1",
        "..-." => "F",
        "...." => "H",
        ".---" => "J",
        "--.-" => "Q",
        "-..-." => "/",
        ".-.." => "L",
        "..." => "S",
        "---" => "O",
        "-.-" => "K",
        ".-." => "R",
        "..-" => "U",
        "-.." => "D",
        ".--" => "W",
        "--." => "G",
        "-." => "N",
        "--" => "M",
        ".." => "I",
        ".-" => "A",
        "-" => "T",
        "." => "E",
    ];

$str = str_replace('  ', ' ', $str);
$strToArray = explode(' ', $str);

$answer = "";

for($i = 0; $i < count($strToArray); $i++) {
     foreach($morse as $code => $letter)
     {
         if(($strToArray[$i]) == $code)
         {
             $answer .= $letter;
         } 
         else if(($strToArray[$i]) == "")
         {
             $answer .= "";
         }
     }
 }
 return $answer;
}



